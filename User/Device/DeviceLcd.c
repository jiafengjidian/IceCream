static void vGpioInit(void)
{
	RCC->AHB1ENR |= 0x01;
	GPIOA->MODER &= ~0x03;
	GPIOA->MODER |= 0x01;
	GPIOA->OTYPER &= ~0x01;
	GPIOA->OSPEEDR |= 0x03;
	GPIOA->PUPDR &= ~0x03;
	GPIOA->PUPDR |= 0x01;
}

static void vReset(void)
{
	GPIOA->BSRRH = 0x01;
	vSysTimerDelay(100);
	GPIOA->BSRRL = 0x01;
	vSysTimerDelay(100);
}

