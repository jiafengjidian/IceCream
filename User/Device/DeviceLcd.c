static const uint8_t FontArray[][32];
static const uint8_t AsciiFontArray[][16];

static void vLcdOpenWindow(uint16_t x0, uint16_t y0, uint16_t xSize, uint16_t ySize);

void vLcdFullRect(uint16_t x0, uint16_t y0, uint16_t width, uint16_t height, uint16_t color)
{
	uint32_t i = width * height ;
	
	vLcdOpenWindow(x0, y0, width, height);
	
	for (; i--; ) {
		LcdWriteData(color);
	}
}

void vLcdDisplayAsciiString(uint16_t x0, uint16_t y0, const char *str)
{
	while (*str != '\0') {
		vLcdDisplayAscii(x0, y0, *str);
		str ++;
		x0 += 8;
	}
}

void vLcdDisplayAscii(uint16_t x0, uint16_t y0, char c)
{
	uint8_t i, j, temp;
	c -= ' ';
	vLcdOpenWindow(x0, y0, 8, 16);
	for (i = 0; i < 16; i ++)
	{
		temp = AsciiFontArray[c][i];
		for (j = 0; j < 8; j ++) 
		{
			if (temp & 0x80) {
				LcdWriteData(0xF800);
			} else {
				LcdWriteData(0xFFFF);
			}
			temp <<= 1;
		}
	}
}

void vLcdDisplayChar(uint16_t x0, uint16_t y0, uint16_t index)
{
	uint8_t i, j;
	uint8_t c;
	const uint8_t *p = FontArray[index];
	vLcdOpenWindow(x0, y0, 16, 16);
	
	for (i = 0; i < 32; i += 2) 
	{
		c = p[i];
		for (j = 0; j < 8; j ++) 
		{
			if (c & 0x80) {
				LcdWriteData(0xF800);
			} else {
				LcdWriteData(0xFFFF);
			}
			c <<= 1;
		}
		c = p[i + 1];
		for (j = 0; j < 8; j ++) 
		{
			if (c & 0x80) {
				LcdWriteData(0xF800);
			} else {
				LcdWriteData(0xFFFF);
			}
			c <<= 1;
		}
	}
}

static void vLcdOpenWindow(uint16_t x0, uint16_t y0, uint16_t xSize, uint16_t ySize)
{
	LcdWriteCommand(0x2A);
	LcdWriteData(x0 >> 8);
	LcdWriteData(x0 & 0xFF);
	LcdWriteData((x0 + xSize - 1) >> 8);
	LcdWriteData((x0 + xSize - 1) & 0xFF);
	
	LcdWriteCommand(0x2B);
	LcdWriteData(y0 >> 8);
	LcdWriteData(y0 & 0xFF);
	LcdWriteData((y0 + ySize - 1) >> 8);
	LcdWriteData((y0 + ySize - 1) & 0xFF);
	
	LcdWriteCommand(0x2C);
}

